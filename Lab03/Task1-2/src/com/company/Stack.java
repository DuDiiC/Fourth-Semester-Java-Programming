package com.company;

import java.util.ArrayList;

public class Stack extends ArrayList<Object> {

    public Stack() {
        super();
    }

    @Override
    public boolean add(Object o) {
        return super.add(o);
    }

    @Override
    public boolean isEmpty() {
        return super.isEmpty();
    }

    @Override
    public Object get(int index) {
        return super.get(index);
    }
}
